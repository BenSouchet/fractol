#include "libft.h"

char	*ft_strcpy(char *s1, const char *s2)
{
	size_t n;

	n = 0;
	while (s2[n])
	{
		s1[n] = s2[n];
		n++;
	}
	s1[n] = '\0';
	return (s1);
}

void	ft_cpy(char *s1, char *s2)
{
	int i;

	i = -1;
	while (s1[++i] != 0)
		s2[i] = s1[i];
	s2[i] = 0;
}
